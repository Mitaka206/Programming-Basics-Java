public class forLoops {
    public static void main(String[] args) {

        int n=0;
                                //(променлива; условие; изпълнение)
        for (n=1; n<=5; n++)//вкарваме променливата директно в условието и задаваме какво да се изпълнява
        {
            System.out.println(n);
        }
    }
}
